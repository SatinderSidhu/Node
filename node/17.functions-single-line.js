// Arrow function shortens the length of the function 

// normal function
const square = function(x) {return x*x};
console.log(square(2));

// Convert the method Arrow function 
const square2 = (x) => {return x*x};
console.log(square2(2));

// even shorten the function by taking of ()
const square3 = x => {return x*x};
console.log(square3(2));
